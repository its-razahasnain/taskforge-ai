def print_equalto_seperator(n):
    print("=" * n)


def print_dash_seperator(n):
    print("-" * n)


def print_new_line():
    print("\n")


def task_len(task):
    return len(task) + 20


def show_message(message):
    print_new_line()
    print_dash_seperator(30)
    print(message)
    print_dash_seperator(30)
    print_new_line()
